
package basico;

public class IncrementoDecremento {

	public static void main(String... args) {
		int i = 1;
		int a = 2;
		int b = 3;

		// Pré incremento
		++i;
		// Pré decremento
		--i;

		// Pós incremento
		i++;
		// Pós decremento
		i--;

		i *= 2;
		i /= 2;

		i += 2;
		i -= 2;
		i = a = b;

	}

}
